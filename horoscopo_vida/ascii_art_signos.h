    // Funci√≥n para mostrar el arte de un signo
    static void mostrar_arte_signo(const string& signo) {
        if (arte_ascii.empty()) {
            inicializar_arte();
        }
        
        if (arte_ascii.find(signo) != arte_ascii.end()) {
            cout << "\n" << arte_ascii[signo] << "\n";
        } else {
            cout << "\n‚ú® " << signo << " ‚ú®\n";
        }
    }
    
    // Funci√≥n para mostrar arte animado
    static void mostrar_arte_animado(const string& signo) {
        if (arte_ascii.empty()) {
            inicializar_arte();
        }
        
        cout << "\nüåü Revelando tu signo zodiacal... üåü\n";
        
        // Efecto de "cargando"
        for (int i = 0; i < 3; i++) {
            std::this_thread::sleep_for(std::chrono::milliseconds(500));
            cout.flush();
            this_thread::sleep_for(chrono::milliseconds(500));
        }
        
        cout << "\n";
        mostrar_arte_signo(signo);
    }
    
